char *disasm(uint32_t *instr, size_t size) {
	char *dis = NULL;
	uint8_t opcode = (uint8_t)instr[0];
	if (size > 1) {
		uint32_t value = size == 4 ? instr[1] | (instr[2] << 8) : instr[1];
		if (size == 4) {
			dis = malloc(8+strlen(str_instrs[opcode])+2+20);
			if (dis) {
				sprintf(dis, str_instrs[opcode], value);
			}
		}
		else {
			dis = malloc(8+strlen(str_instrs[opcode])+20);
			if (dis) {
				sprintf(dis, str_instrs[opcode], (int8_t)value); // value is signed
			}
		}
	}
	else {
		dis = malloc(8+strlen(str_instrs[opcode])+20);
		if (dis) {
			sprintf(dis, str_instrs[opcode]);
		}
	}
	return dis;
}
